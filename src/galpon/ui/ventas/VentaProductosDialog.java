    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int fila = this.tablaProductos.getSelectedRow();
        if (this.tablaProductos.getSelectedRowCount() < 1) {
            JOptionPane.showMessageDialog(rootPane, "Debe Seleccionar un Producto");
            return;
        }
        if (txtCantidad.getText().equals("") || txtCantidad.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debe Seleccionar una cantidad");
            return;
        }
//frmVenta.comboProducto.setSelectedItem(tablaProductos.getValueAt(fila, 1));
        Integer valor = Integer.parseInt(tablaProductos.getValueAt(fila, 3).toString());
        DefaultTableModel modelo = (DefaultTableModel) VentasForm.tablaProducto.getModel();
        VentasForm.tablaProducto.setModel(modelo);
        modelo.addRow(new Object[]{
            tablaProductos.getValueAt(fila, 1).toString(), txtCantidad.getText(), tablaProductos.getValueAt(fila, 2).toString(), tablaProductos.getValueAt(fila, 3).toString(), valor * Integer.parseInt(txtCantidad.getText())
        // tablaProductos.getValueAt(fila, 1).toString(),txtCantidad.getText(),tablaProductos.getValueAt(fila, 2).toString(),tablaProductos.getValueAt(fila, 3).toString(),""

        });
        //comboProducto.setSelectedItem("Seleccionar");
        //txtCantidad.setText("");
        this.dispose();

    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
        // TODO add your handling code here:
          char num = evt.getKeyChar();
        if ((num < '0' || num > '9')) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCantidadKeyTyped

