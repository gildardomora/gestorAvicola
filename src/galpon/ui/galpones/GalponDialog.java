    private void cargarinformacion() {
        //SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");  //Ajusta el formato del DateChooser
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date fecha;
        cargarCombos();
        switch (AdminGalponForm.accion) {
            case "nuevo":
                txtNumGalpon.requestFocus();
                comboTipoAve.setSelectedItem("Seleccionar");
                Calendar actual = new GregorianCalendar();//para asignar la fecha actual
                jDateFechaApe.setCalendar(actual);
                comboEstado.setSelectedItem("seleccionar");
                break;
            case "editar":
                txtNumGalpon.requestFocus();
                txtNumGalpon.setText(AdminGalponForm.datostabla[0]);
                comboTipoAve.setSelectedItem(AdminGalponForm.datostabla[1]);
                try {
                    fecha = formatoFecha.parse((String) AdminGalponForm.datostabla[2]);
                    jDateFechaApe.setDate(fecha);
                } catch (ParseException ex) {
                    Logger.getLogger(GalponDialog.class.getName()).log(Level.SEVERE, null, ex);
                }
                txtSaldoAves.setText(AdminGalponForm.datostabla[3]);
                AtxtDescripcion.setText(AdminGalponForm.datostabla[4]);
                  comboEstado.setSelectedItem(AdminGalponForm.datostabla[5]);
                break;
            default:
                JOptionPane.showMessageDialog(rootPane, " ocurrio un error");
        }
    }
    private void cargarCombos() {// carga datos de la BD y los pone en los combobox
        fAdminGalpon consulta = new fAdminGalpon();
        comboTipoAve.removeAllItems();
        comboTipoAve.addItem("Seleccionar");
        ArrayList<String> listaAve = new ArrayList<String>();
        listaAve = consulta.llenar_comboAve();
        for (int i = 0; i < listaAve.size(); i++) {
            comboTipoAve.addItem(listaAve.get(i));
        }              
        comboEstado.removeAllItems();
        comboEstado.addItem("Seleccionar");
        comboEstado.addItem("ACTIVO");
        comboEstado.addItem("CERRADO");
        comboEstado.addItem("SUSPENDIDO");
        comboEstado.addItem("MANTENIMIENTO");     

    }//cierre metodo


