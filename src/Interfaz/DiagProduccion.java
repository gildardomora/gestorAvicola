    void cargarinformacion() {
        // lblNumGalpon.setText(frmProduccion.datostabla[1]);
        llenarCombo(comboGalpon, "id_galpon", "galpon");
        llenarCombo(comboProducto, "nombre_producto", "producto");
        Calendar actual = new GregorianCalendar();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        Date fecha = null;

        switch (frmProduccion.accion) {
            case "nueva":
                // Calendar actual = new GregorianCalendar();//para asignar la fecha actual
                jDateFecha.setCalendar(actual);
                break;
            case "editar":
                comboGalpon.setSelectedItem(frmProduccion.datostabla[1]);
                comboProducto.setSelectedItem(frmProduccion.datostabla[2]);
                txtCantidad.setText(frmProduccion.datostabla[3]);

                try {
                    fecha = formatoFecha.parse((String) frmProduccion.datostabla[4]);
                } catch (ParseException ex) {
                    Logger.getLogger(DiagProduccion.class.getName()).log(Level.SEVERE, null, ex);
                }
                jDateFecha.setDate(fecha);
                //  AtxtObservaciones.setText(frmProduccion.datostabla[5]);
                break;
        }
    }

