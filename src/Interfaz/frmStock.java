    private void tablaStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaStockMouseClicked
        // TODO add your handling code here:
        int filaSel = tablaStock.rowAtPoint(evt.getPoint());
        lblidProducto.setText(tablaStock.getValueAt(filaSel, 0).toString());
        lblNombre.setText(tablaStock.getValueAt(filaSel, 1).toString());
        txtCantStock.setText(tablaStock.getValueAt(filaSel, 2).toString());
        btnEditar.setEnabled(true);
    }//GEN-LAST:event_tablaStockMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (btnEditar.getText().equals("Editar")) {
            txtCantStock.requestFocus();
            btnCancelar.setEnabled(true);
            btnEditar.setText("Guardar");
            txtCantStock.setEnabled(true);
            lblNombre.setEnabled(true);

        } else {
         
            btnEditar.setText("Editar");
            btnCancelar.setEnabled(false);
            btnEditar.setEnabled(false);
            txtCantStock.setEnabled(false);
            lblNombre.setEnabled(false);
            fProducto func =new fProducto();
            mProducto datos = new mProducto();            
            datos.setIdProducto(Integer.parseInt(lblidProducto.getText()));
            datos.setStock(Integer.parseInt(txtCantStock.getText()));
            func.editarStock(datos);
            func.listarStock("");
        }


    }//GEN-LAST:event_btnEditarActionPerformed

