    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if (lblUsuario.getText().equals("#")){
            JOptionPane.showMessageDialog(null,"Debe Elegir primero un Usuario");
            return;
        }
        
         mUsuario datos = new mUsuario();
        fUsuario func = new fUsuario();
        
        
        if(func.ContarAdmins()==1){try {
            //para impedir que sea eliminado el admin del sistema
            
            if(func.tipo(lblUsuario.getText()).equals("Administrador")){
                JOptionPane.showMessageDialog(rootPane, "Solo hay un administrador del Sistema"
                    + "\n no se permite borrar este usuario."
                    + "\n ");
            return;
            }   } catch (SQLException ex) {
                Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        

        datos.setUsuario(lblUsuario.getText());
        int confirmar=JOptionPane.showConfirmDialog(rootPane, "Seguro que quiere eliminar \n el usuario "+lblUsuario.getText()+" ?","Confirmar",2);
        if(confirmar==0){
        if(func.eliminar(datos)){
        JOptionPane.showMessageDialog(rootPane, "Usuario Eliminado Correctamente");
        }
    }
        mostrar("");
        postsql.cierraConexion();
    }//GEN-LAST:event_btnEliminarActionPerformed

