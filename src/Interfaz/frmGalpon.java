    private void btnNuevaEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevaEntradaActionPerformed
        // habilitar campos

        if (btnNuevaEntrada.getText().equals("Nueva")) {
            btnNuevaEntrada.setLabel("Guardar");
            btnEditarEnt.setLabel("Cancelar");
            btnEditarEnt.setEnabled(true);
            txtCantidadEnt.setText("");
            Calendar actual = new GregorianCalendar();
            jDateFechaEnt.setCalendar(actual);
            habilitarcamposEnt();
            // jDateFecha.requestFocus();
            return;
        }

        mEntrada datosEnt = new mEntrada();
        fGalpon func = new fGalpon();

        if (btnNuevaEntrada.getText().equals("Guardar")) {

            if (jDateFechaEnt.getDate() == null) {
                JOptionPane.showMessageDialog(rootPane, "Debes asignar una fecha de Entrada");
                jDateFechaEnt.requestFocus();
                return;
            }
            if (txtCantidadEnt.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debes asignar una Cantidad");
                txtCantidadEnt.requestFocus();
                return;
            }
            Date dateEnt = jDateFechaEnt.getDate();//almacena la fecha en formato Date
            long fechaE = dateEnt.getTime();
            java.sql.Date fechaEnt = new java.sql.Date(fechaE);//establece el formato compatible con sql

            datosEnt.setIdGalpon(Integer.parseInt(lblNoGalpon.getText()));
            datosEnt.setFecha(fechaEnt.toString());
            datosEnt.setCantidad(Integer.parseInt(txtCantidadEnt.getText()));
            datosEnt.setDescripcion(AtxtDescripcionEnt.getText());
            datosEnt.sethSaldo(Integer.parseInt(lblSaldo.getText()));
            func.insertarEntrada(datosEnt);// ejecuta la insercion de datos

//insertar(lblNoGalpon.getText(), "Entrada");//envia el numero del galpon, envia el tipo de movimiento (entrada-salida)
            btnNuevaEntrada.setLabel("Nueva");
            btnEditarEnt.setLabel("Editar");
            btnEditarEnt.setEnabled(false);
            txtCantidadEnt.setText("");
            //jDateFecha.setDateFormatString("");
            deshabilitarcamposEnt();
            frmGalpon(lblNoGalpon.getText());//para recargar el formulario
            mostrarEntrada(lblNoGalpon.getText());
            mostrarSalida(lblNoGalpon.getText());

            fAdminGalpon func2 = new fAdminGalpon();
            func2.listar("");
            return;

        }

        if (btnNuevaEntrada.getText().equals("Actualizar")) {
            Date dateEnt = jDateFechaEnt.getDate();//almacena la fecha en formato Date
            long fechaE = dateEnt.getTime();
            java.sql.Date fechaEnt = new java.sql.Date(fechaE);//establece el formato compatible con sql

            datosEnt.setIdGalpon(Integer.parseInt(lblNoGalpon.getText()));
            datosEnt.setFecha(fechaEnt.toString());
            datosEnt.setCantidad(Integer.parseInt(txtCantidadEnt.getText()));
            datosEnt.setDescripcion(AtxtDescripcionEnt.getText());
            datosEnt.sethSaldo(Integer.parseInt(lblSaldo.getText()));
            datosEnt.setIdEntrada(Integer.parseInt(lblEntrada.getText()));
            func.editarEntrada(datosEnt);
            // editar(lblNoGalpon.getText(), lblEntrada.getText(), "entrada");//envia el numero del galpon, envia el ID de la entrada
            btnNuevaEntrada.setLabel("Nueva");
            btnEditarEnt.setLabel("Editar");
            txtCantidadEnt.setText("");
            //jDateFecha.setDateFormatString("");
            deshabilitarcamposEnt();
            frmGalpon(lblNoGalpon.getText());
            mostrarEntrada(lblNoGalpon.getText());

            mostrarSalida(lblNoGalpon.getText());

        }


    }//GEN-LAST:event_btnNuevaEntradaActionPerformed

