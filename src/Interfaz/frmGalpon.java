    private void tablaEntradaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEntradaMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        Date fecha;

        int filaSel = tablaEntrada.rowAtPoint(evt.getPoint());

        try {
            //        jDateFecha.setDateFormatString(tablaEntrada.getValueAt(filaSel, 3).toString());
            fecha = formatoFecha.parse((String) tablaEntrada.getValueAt(filaSel, 3));
            jDateFechaEnt.setDate(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(frmGalpon.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtCantidadEnt.setText(tablaEntrada.getValueAt(filaSel, 4).toString());
        AtxtDescripcionEnt.setText(tablaEntrada.getValueAt(filaSel, 5).toString());
        lblEntrada.setText(tablaEntrada.getValueAt(filaSel, 6).toString());
        btnEditarEnt.setEnabled(true);
    }//GEN-LAST:event_tablaEntradaMouseClicked

